## Suspicious Transaction Reporting (STR)

### When to File STR

**File STR When:**
- You know or suspect funds are proceeds of crime
- Transaction structure suggests laundering
- Customer behavior raises concerns
- Pattern inconsistent with profile
- Attempted transaction thwarted
- Customer refuses reasonable CDD
- You have reasonable grounds for suspicion

**Important:** You do NOT need to prove anything. Reasonable suspicion is enough.

### STR Filing Process

**STEP 1: Recognize Suspicious Activity**
- Use red flags and indicators
- Trust professional judgment
- Consider totality of circumstances
- Document observations immediately

**STEP 2: Internal Escalation**
- Report to AML Compliance Officer
- Provide all relevant information
- Include documentation and evidence
- Share concerns and reasoning

**STEP 3: Investigation and Decision**
- AML Officer reviews information
- Additional investigation if needed
- Consult with management
- Decision to file or not
- Document decision reasoning

**STEP 4: File STR with FIC**
- Complete goAML online system
- File within prescribed timeframe
- Include all required information
- Attach supporting documentation
- Obtain filing reference number

**STEP 5: Confidentiality**
- Do NOT inform customer
- Continue normal service
- Process transactions normally (unless instructed otherwise)
- No indication STR filed
- Avoid "tipping off"

**STEP 6: Maintain Records**
- Keep copy of STR
- Document all related information
- Preserve evidence
- Await FIC guidance if any

### STR Content Requirements

**Reporting Party Information:**
- Casino name and address
- Accountable institution number
- Contact person details
- Relationship to customer

**Subject Information:**
- Full personal details
- Identification documentation
- Contact information
- Description (if no ID)
- Associates involved

**Transaction Details:**
- Date, time, location
- Transaction type and amount
- Payment methods used
- Gaming activity observed
- Duration and pattern

**Suspicion Basis:**
- Specific red flags identified
- Behavioral concerns noted
- Pattern analysis
- Risk assessment
- Reasoning for suspicion

**Supporting Evidence:**
- Copies of documentation
- Surveillance footage references
- Transaction records
- Previous reports on same customer
- Intelligence from other sources

### STR Timeline

**Priority Reporting:**
- Terrorism financing: Immediately (same day)
- Serious organized crime: Immediately
- Ongoing laundering: Within 24 hours

**Standard Reporting:**
- Most STRs: Within 15 business days of suspicion

**Best Practice:**
- File as soon as suspicion forms
- Don''t delay for perfect information
- Err on side of over-reporting
- Never delay out of uncertainty

## Cash Threshold Reporting (CTR)

### CTR Requirements

**Report ALL Cash Transactions Above R24,999.99:**
- Single transactions
- Multiple aggregated transactions
- Same customer, same day
- Related parties combined

**Report To:**
- Financial Intelligence Centre (FIC)
- Via goAML system
- Within prescribed timeframe

**CTR vs. STR:**
- CTR = automatic reporting (no suspicion needed)
- STR = based on suspicion
- Some transactions require BOTH
- File both if applicable

### CTR Filing Process

**Information Required:**
- Customer identification details
- Transaction amount and type
- Date and time
