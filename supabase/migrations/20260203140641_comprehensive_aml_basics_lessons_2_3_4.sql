## Specific Role-Based Recognition

### Cashiers and Cage Staff

**Your Unique Vantage Point:**
- Handle all monetary transactions
- See payment method changes
- Observe customer behavior during transactions
- Process chips and cheques

**Watch For:**
- Multiple transactions just below R25,000
- Customer purchasing chips at multiple windows
- Requests to change payout method
- Large amounts of small-denomination bills
- Dirty, odorous, or unusually packaged cash
- Foreign currency without logical explanation
- Customer asking detailed questions about reporting
- Nervousness during identification process

**Best Practices:**
- Take time to observe cash being counted
- Note condition and denomination of bills
- Ask purpose of transaction for amounts >R50,000
- Request identification for transactions >R5,000
- Document any reluctance or evasion
- Trust your instincts

### Gaming Staff (Dealers, Supervisors)

**Your Unique Vantage Point:**
- Observe actual gambling behavior
- See chip movements and transfers
- Watch player interactions
- Notice playing patterns

**Watch For:**
- Large chip purchases with minimal play
- Players not paying attention to game
- Chip transfers between players
- Obviously poor strategic decisions
- Players losing without concern
- Communication/signals between players
- Very short sessions with large buy-ins
- Focus on cashing out rather than playing

**Best Practices:**
- Monitor play-to-buy-in ratios
- Note unusual chip movements
- Report player collusion signs
- Document minimal gambling patterns
- Communicate concerns to pit boss

### Security Personnel

**Your Unique Vantage Point:**
- Observe overall casino floor
- See customer arrivals and departures
- Notice coordination between individuals
- Monitor surveillance footage

**Watch For:**
- Groups arriving together
- Coordinated transaction patterns
- Individuals meeting at casino (not gambling)
- Drop-offs and pick-ups
- Suspicious packages or briefcases
- People directing others from distance
- Unusual vehicle activity
- Known criminals or associates

**Best Practices:**
- Track group movements
- Note license plates of suspicious vehicles
- Review surveillance for pattern confirmation
- Document coordination indicators
- Share intelligence with AML team

### VIP and Player Development

**Your Unique Vantage Point:**
- Know customers personally
- Understand claimed wealth source
- Observe lifestyle consistency
- Monitor credit and play patterns

**Watch For:**
- Source of wealth unclear or inconsistent
- Lifestyle doesn''t match claimed occupation
- Credit usage not proportional to play
- Reluctance to provide background information
- Play patterns changing suddenly
- References to business in high-risk sectors
- International connections to risky jurisdictions
- Media mentions or adverse information

**Best Practices:**
- Conduct thorough background research
- Verify source of wealth independently
- Monitor play vs. credit continuously
- Document all intelligence gathered
- Question inconsistencies professionally

