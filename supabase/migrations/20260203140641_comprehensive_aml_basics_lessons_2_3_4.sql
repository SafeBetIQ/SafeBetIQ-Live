## Implementing AML Controls in Daily Operations

This lesson provides step-by-step procedures for conducting customer due diligence, monitoring transactions, maintaining records, and reporting suspicious activity. Consistent application of these procedures is your legal obligation and protection.

## Customer Due Diligence (CDD) Procedures

### When CDD Is Required

**Prescribed Transactions (ALWAYS require CDD):**

**1. Cash Transactions:**
- Single cash transaction above R24,999.99
- Multiple cash transactions totaling above R24,999.99 in one day
- Electronic funds transfers above R24,999.99

**2. Business Relationships:**
- Opening player account
- Establishing credit facility
- VIP program enrollment
- Junket arrangement

**3. Suspicious Transactions:**
- ANY transaction deemed suspicious (regardless of amount)
- Pattern indicating possible money laundering
- Customer behavior raising concerns

**4. Unable to Apply CDD:**
- When you cannot verify customer identity
- When customer refuses to provide information
- When information appears fraudulent

### Step-by-Step CDD Process

**STEP 1: Customer Identification**

**Obtain Following Information:**
- Full legal name (including maiden name if applicable)
- Date of birth
- South African ID number OR passport number (foreigners)
- Nationality
- Residential address
- Contact details (phone, email)
- Occupation
- Employer name and contact

**For Legal Entities (Companies):**
- Registered company name
- Registration number
- Registered address
- Beneficial owners (natural persons with 5%+ ownership or control)
- Nature of business
- Directors and signatories

**STEP 2: Verification of Identity**

**Acceptable Documents - South African Citizens:**
- Green barcoded ID book (original)
- Smart ID card (original)
- Valid passport (original)

**Acceptable Documents - Foreign Nationals:**
- Valid passport (original)
- Valid temporary residence permit
- Valid permanent residence permit
- Valid work permit

**Verification Process:**
1. Examine original document (NO photocopies for verification)
2. Check document appears genuine (not altered or counterfeit)
3. Verify photo matches person
4. Check expiry date (passports, permits must be valid)
5. Record document details
6. Make clear copy for records
7. Check against HANIS system if available

**Red Flags in Documents:**
- Document appears altered or tampered
- Photo doesn''t match person
- Expired documents
- Poor quality counterfeit
- Customer reluctant to present original
- Multiple IDs presented
- Information inconsistencies between documents

**STEP 3: Verify Residential Address**

**Acceptable Proof of Address (not older than 3 months):**
- Municipal bill (rates, electricity, water)
- Bank statement
- Telkom/cellular phone account
- Retail store account statement
- Insurance policy document
- SARS correspondence
- Lease agreement (with lessor certification if needed)

**If Customer Cannot Provide:**
- Visit home address (senior management approval)
- Obtain affidavit from traditional leader or ward councillor
- Check against population register
- Consider not establishing relationship

**STEP 4: Risk Assessment**

**Assess Customer Risk Level:**

**Low Risk Criteria:**
- South African citizen/resident
- Salaried employee of reputable company
- Reasonable transaction size for profile
- No PEP connections
- No high-risk jurisdiction ties
- Transparent source of funds
- Logical transaction purpose

**Medium Risk Criteria:**
- Self-employed or business owner
- Cash-intensive business
- Irregular income pattern
- Foreign national from medium-risk country
- Large transactions relative to stated income
- Limited background information available

**High Risk Criteria:**
- Politically exposed person (PEP)
- High-risk jurisdiction connection
- Sanctioned country ties
- Source of wealth unclear
- Cash-intensive business owner (forex, cash-in-transit, second-hand dealer)
- Adverse media or intelligence
- Unusual transaction patterns

**STEP 5: Source of Funds Verification**

**For Transactions Above R100,000, Always Verify Source:**

**Acceptable Source Documentation:**
- Payslips (employment income)
- Bank statements (showing deposit source)
- Sale agreements (asset sales)
- Inheritance documentation (estate distributions)
- Business financial statements (business proceeds)
- Loan agreements (borrowed funds)
- Investment statements (investment liquidation)

**Questions to Ask:**
- "What is the source of these funds?"
- "How did you acquire this money?"
- "Can you provide documentation?"
- "Is this money from your business or employment?"

**Red Flags in Source Explanations:**
- Vague or evasive answers
- Story changes when questioned
- Source doesn''t match profile
- Amount implausible for stated source
- Reluctance to provide documentation
- Different explanations over time

**STEP 6: Ongoing Monitoring**

**Continuous CDD Requirements:**
- Review customer activity regularly
- Update information at least annually
- Refresh documentation when expired
- Reassess risk if behavior changes
- Question unusual transactions
- File STR if concerns develop

## Enhanced Due Diligence (EDD)

### When EDD Is Required

**Mandatory EDD Scenarios:**
- Politically exposed persons (PEPs)
- High-risk jurisdiction customers
- Customers from sanctioned countries
- High-risk business relationships
- Unusual or very large transactions
- Negative media or intelligence

### EDD Additional Requirements

**Enhanced Identification:**
- Senior management approval required
- Source of wealth verification (not just funds)
- Purpose of relationship documentation
- Expected activity and transaction patterns
- Beneficial ownership fully disclosed
- Independent verification of information

**Source of Wealth vs. Source of Funds:**

**Source of Wealth (overall financial position):**
- How did customer accumulate wealth generally?
- Career earnings, inheritances, business success, investments
- Documentation: tax returns, financial statements, asset registers

**Source of Funds (specific transaction):**
- Where did money for THIS transaction come from?
- Sale proceeds, salary, business income, loan, investment liquidation
- Documentation: specific to current transaction

**Enhanced Monitoring:**
- More frequent review (monthly for high-risk)
- Detailed transaction analysis
- Adverse media monitoring
- Ongoing verification of source
- Immediate escalation of concerns

