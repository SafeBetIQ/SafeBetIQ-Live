## Identifying Money Laundering in Daily Operations

This lesson provides detailed guidance on recognizing money laundering attempts in your daily work. Effective detection requires understanding typologies, recognizing patterns, and trusting professional judgment.

## Casino-Specific Money Laundering Typologies

### Typology 1: The Quick Flip

**Method:**
Criminal purchases large amount of chips with cash, plays briefly, cashes out via different payment method.

**Step-by-Step:**
1. Arrives with R100,000-R500,000 cash
2. Purchases chips at cashier or cage
3. Moves to gaming floor
4. Plays minimal amount (R5,000-R15,000)
5. Returns to cage within 1-2 hours
6. Requests payout via cheque or wire transfer
7. Leaves immediately

**Red Flags:**
- Large cash buy-in
- Very short gaming session
- Minimal actual gambling observed
- Different payout method requested
- Appears uninterested in games
- Focus on completing transaction

**Risk Level:** HIGH - Classic placement and layering

**Your Response:**
- Document time between buy-in and cash-out
- Note actual play observed
- Calculate play-to-buy-in ratio
- Question reason for payout method change
- File STR if pattern clear

**Real Example:**
*Customer "John" purchased R250,000 in chips at 14:30 with cash. Played blackjack for 45 minutes, wagered approximately R10,000 total. At 15:30 requested cash-out of R242,000 via bank transfer. When questioned about source of cash, became defensive and agitated. STR filed.*

### Typology 2: The Group Operation

**Method:**
Multiple people working together to structure transactions below reporting threshold.

**Step-by-Step:**
1. 5-10 people arrive together (or coordinated timing)
2. Each purchases chips in amount just under R25,000
3. Aggregate transactions total R200,000-R500,000
4. Minimal gambling by all members
5. Regroup and combine chips
6. One person cashes out large amount
7. Group leaves together

**Red Flags:**
- Multiple people arriving together
- Similar transaction amounts (R20,000-R24,000)
- Coordinated timing
- Limited communication with staff
- Similar demographics or appearance
- Minimal gambling by all
- Chip consolidation observed
- Group departure

**Risk Level:** VERY HIGH - Structured placement operation

**Your Response:**
- Treat group as single customer for reporting
- Aggregate transactions even if individually below threshold
- Document group dynamics and coordination
- Note any communication or direction between members
- Question relationships if appropriate
- File STR immediately

**Real Example:**
*Seven individuals arrived 15 minutes apart. Each purchased exactly R24,000 in chips. All played same slot machines briefly, minimal wagering. Observed consolidating chips in restroom area. One member cashed out R162,000 via cheque. Surveillance footage confirmed coordination. Group arrested for drug trafficking money laundering.*

### Typology 3: The Losing Scheme

**Method:**
Criminals deliberately lose to confederates to transfer value.

**Step-by-Step:**
1. Criminal A has dirty money to launder
2. Criminal B is confederate (accomplice)
3. Criminal A purchases chips with dirty cash
4. Both sit at same table game
5. Criminal A deliberately loses to Criminal B
6. Criminal B leaves with winnings
7. Criminal B cashes out - money appears as gambling winnings

**Red Flags:**
- Player making obviously poor decisions
- No reaction to losing
- Excessive losses without concern
- Another player winning consistently against them
- Players arriving/leaving together
- Communication between players
- One player coaching another
- Unusual betting patterns

**Risk Level:** HIGH - Layering through deliberate loss

**Your Response:**
- Gaming staff monitor play patterns
- Note relationship between players
- Document unusual betting or playing
- Question chip transfers at tables
- Report suspicious losses to pit boss
- File STR if pattern evident

**Real Example:**
*Two blackjack players observed over 3 hours. Player A made consistently illogical plays, lost R180,000 to Player B. Player B won R165,000. Both left together. Surveillance review showed they arrived together and communicated frequently. STR filed, investigation revealed organized crime connection.*

### Typology 4: The False Win Claim

**Method:**
Criminal claims casino winnings as source of funds for large purchase.

**Step-by-Step:**
1. Criminal launders money through casino (minimal play)
2. Obtains casino cheque or wire transfer
3. Deposits in bank account
4. Bank questions source of funds
5. Criminal provides casino payout as "proof"
6. Uses funds to purchase property, vehicles, business

**Red Flags:**
- Requests for detailed payout documentation
- Asking for win/loss statements
- Large payouts not proportional to observed play
- Player specifically mentions needing documentation
- Recent customer with large payout
- Multiple casinos involved in short time

**Risk Level:** MEDIUM-HIGH - Integration stage

**Your Response:**
- Question unusual documentation requests
- Verify play history before providing win/loss statements
- Note if payout seems disproportionate to play
- Coordinate with player tracking systems
- Be cautious of accommodating unusual requests
- File STR if circumstances suspicious

**Real Example:**
*Customer requested detailed win/loss statement for "tax purposes" after R300,000 payout. Review of play showed only R50,000 wagered total. Customer had visited 4 casinos in 2 weeks, all with similar patterns. Investigation revealed customer was using false winnings claims to deposit proceeds from corruption scheme.*

### Typology 5: The Third-Party Payment

**Method:**
Using intermediaries to disguise source of funds.

**Step-by-Step:**
1. Criminal cannot use own identity (wanted, sanctioned, etc.)
2. Recruits nominee (often desperate person needing money)
3. Nominee conducts casino transactions using criminal''s money
4. Nominee provides ID and information
5. Criminal directs all actions behind scenes
6. Nominee receives small fee
7. Criminal receives cleaned funds

**Red Flags:**
- Customer seems to be acting on behalf of another
- Receives phone calls during transactions with instructions
- Unclear about source of funds
- Income/lifestyle doesn''t match transaction size
- Nervous or coached responses
- References "my boss" or "friend"
- Someone else visible nearby directing

**Risk Level:** HIGH - Identity concealment

**Your Response:**
- Question who beneficial owner of funds is
- Ask about relationship to source of money
- Document suspicious third-party indicators
- Refuse transaction if nominee status clear
- Report to supervisor immediately
- File STR

**Real Example:**
*Young unemployed woman attempted R150,000 cash purchase. When questioned, repeatedly looked at older man across casino floor, made phone calls before answering questions. Eventually admitted "helping a friend." Transaction declined, STR filed. Investigation revealed "friend" was sanctioned individual on OFAC list.*

## Indicator Categories and Risk Scoring

### Customer Profile Indicators

**High Risk (+3 each):**
- Politically Exposed Person (PEP)
- High-risk jurisdiction connection
- Sanctioned country ties
- Known criminal associates
- Adverse media mentions

**Medium Risk (+2 each):**
- Cash-intensive business owner
- Unexplained wealth
- Frequent international travel
- Multiple addresses/identities
- Offshore account connections

**Low Risk (+1 each):**
- New customer (no history)
- Large transactions relative to profile
- Vague occupation information
- Reluctance to provide details

### Transaction Indicators

**High Risk (+3 each):**
- Structuring pattern evident
- Minimal gambling with large buy-in
- Different payout method from deposit
- Coordinated group activity
- Just below reporting threshold repeatedly

**Medium Risk (+2 each):**
- Unusual payout requests
- Multiple transactions same day
- Large cash transaction
- Chip walking observed
- Rapid in-and-out pattern

**Low Risk (+1 each):**
- Large transaction (but proportional to profile)
- Cash preference (but consistent with history)
- Weekend high-roller (verified regular)

### Behavioral Indicators

**High Risk (+3 each):**
- Extreme nervousness
- Aggressive when questioned
- Knows AML procedures too well
- Attempts to avoid identification
- Leaves when questioned

**Medium Risk (+2 each):**
- Evasive answers
- Inconsistent information
- Defensive responses
- Excessive interest in procedures
- Urgent without clear reason

**Low Risk (+1 each):**
- Minor nervousness (normal for large transaction)
- Asks procedural questions
- First-time large transaction

### Risk Scoring System

**Total Score Interpretation:**
- **0-3 points:** Low risk - proceed with normal procedures, document
- **4-7 points:** Medium risk - enhanced attention, document thoroughly, supervisor notification
- **8-12 points:** High risk - detailed questioning, senior management notification, consider STR
- **13+ points:** Very high risk - immediate supervisor involvement, strong consideration for STR

**Important:** This is a GUIDE only. Use professional judgment. A single high-risk indicator may be enough to file STR.

