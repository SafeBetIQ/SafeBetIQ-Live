## Professional Ethics and Integrity

### Your Professional Duty

**You are a guardian of the financial system:**

- Your vigilance prevents crime
- Your reports save lives
- Your professionalism protects society
- Your integrity matters

**Code of Conduct:**
- Place compliance above personal gain
- Report concerns regardless of customer value
- Maintain independence and objectivity
- Resist pressure to overlook violations
- Support colleagues in compliance efforts

### Confidentiality Obligations

**What You MUST Keep Confidential:**
- That you''ve filed an STR
- That customer is under investigation
- Details of AML investigations
- Intelligence from authorities
- Specific techniques used in detection

**Tipping Off Is a CRIME:**
- Never inform customer they''re suspected
- Don''t warn them about reports filed
- Don''t change behavior that alerts customer
- Maintain normal interaction
- Process transactions normally unless instructed otherwise

### Ethical Dilemmas

**Scenario: VIP Customer**
Your best VIP customer, who generates significant revenue, exhibits concerning patterns. Reporting might lose the business.

**Right Action:** Report. Revenue never justifies enabling crime.

**Scenario: Colleague Involved**
You suspect a colleague is facilitating money laundering.

**Right Action:** Report to AML officer or compliance. Loyalty to law supersedes personal relationships.

**Scenario: Pressure from Management**
Manager suggests not reporting borderline case to avoid regulatory attention.

**Right Action:** Report anyway. You have personal legal liability. Document pressure and report through whistleblower channels if necessary.

