## Red Flags and Warning Signs

### Transaction Red Flags

**Cash Red Flags:**
- Large amounts of cash (especially R50, R100 notes)
- Dirty, worn, or odorous cash
- Multiple small-denomination bills
- Foreign currency without logical explanation
- Cash bundled in unusual ways (rubber bands, bank wrappers from different institutions)

**Transaction Patterns:**
- Just below R25,000 threshold repeatedly
- Reluctance to provide identification
- Urgency without logical reason
- Multiple transactions same day
- Coordinated group transactions

**Payment Method Red Flags:**
- Requesting different method for payout than deposit
- Multiple payment sources
- Third-party payments
- Offshore accounts involved
- Cryptocurrency conversions

### Behavioral Red Flags

**Customer Behavior:**
- Nervousness during routine transactions
- Excessive interest in reporting requirements
- Attempts to avoid identification
- Angry when asked for ID
- Overly familiar with AML procedures (professional launderer)

**Gambling Behavior:**
- Purchasing chips without gambling
- Minimal play with large buy-in
- No interest in winning
- Losses accepted without concern
- Focus on cashing out rather than playing

**Relationship Red Flags:**
- Refuses to provide information about occupation
- Vague about source of funds
- Inconsistent stories
- Background doesn''t match wealth level
- Known criminal associates

### Geographic Red Flags

**High-Risk Jurisdictions:**
- Countries with weak AML controls
- Tax havens
- Sanctioned nations
- Conflict zones
- Known drug trafficking regions

**FATF High-Risk Countries** (examples as of 2024):
- North Korea
- Iran
- Myanmar
- Countries under increased monitoring (grey list)

**South African Context:**
- Cross-border transactions from neighboring countries
- Unexplained wealth from high-corruption areas
- Funds from areas with organized crime presence

