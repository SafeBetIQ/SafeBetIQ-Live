## Professional Training for Financial Crime Prevention

Welcome to this essential training on Anti-Money Laundering (AML) for casino professionals. This module provides comprehensive knowledge of how casinos are used in money laundering schemes and your critical role in preventing financial crime.

## Why AML Training Is Critical

Casinos process billions of rand annually, making them attractive targets for criminals seeking to launder proceeds from serious crimes including drug trafficking, human trafficking, corruption, terrorism financing, and organized crime.

### The Scale of the Problem

**Global Money Laundering:**
- Estimated 2-5% of global GDP ($800 billion - $2 trillion annually)
- Casinos identified as high-risk sector by FATF
- South Africa designated as higher-risk jurisdiction

**Impact on Society:**
- Funds terrorism and organized crime
- Undermines legitimate economy
- Corrupts financial institutions
- Facilitates drug trade and human trafficking
- Destabilizes governments and communities

**Casino Industry Vulnerability:**
- Large cash transactions common
- Anonymous gambling possible
- Rapid movement of funds
- International player base
- Multiple transaction types
- Entertainment environment masks criminal activity

## Legal Framework in South Africa

### Financial Intelligence Centre Act (FICA)

The **Financial Intelligence Centre Act 38 of 2001** is South Africa''s primary AML legislation.

**Key Requirements for Casinos:**

**1. Customer Due Diligence (CDD)**
- Identify and verify all customers conducting prescribed transactions
- Understand nature and purpose of business relationship
- Conduct ongoing monitoring
- Keep records for minimum 5 years

**2. Know Your Customer (KYC)**
- Obtain full name and identity number
- Verify identity using reliable documents
- Verify residential address
- Establish source of funds for large transactions
- Identify beneficial owners of legal entities

**3. Record Keeping**
- Maintain detailed transaction records
- Keep identification documents
- Document risk assessments
- Retain records for 5 years after relationship ends

**4. Suspicious Transaction Reporting**
- Report suspicious transactions to Financial Intelligence Centre (FIC)
- File Suspicious Transaction Reports (STRs) within prescribed timeframes
- Report attempted transactions
- Maintain confidentiality

**5. Cash Threshold Reporting**
- Report cash transactions above R24,999.99
- Report electronic funds transfers above R24,999.99
- File Cash Threshold Reports (CTRs)

### National Gambling Act

**National Gambling Act 7 of 2004** and provincial gambling legislation impose additional obligations:

- Implement AML controls
- Train staff on money laundering risks
- Monitor player behavior
- Cooperate with regulators
- Implement responsible gambling measures
- Report suspicious activity to gambling boards

### Protection of Constitutional Democracy Against Terrorist and Related Activities Act (POCDATARA)

**Terrorist Financing Obligations:**
- Screen against UN sanctions lists
- Report suspected terrorist financing
- Freeze assets of designated entities
- Do not conduct transactions with listed persons

### Penalties for Non-Compliance

**Individual Liability:**
- Criminal prosecution (up to 15 years imprisonment)
- Personal fines (up to R10 million)
- Professional disbarment
- Criminal record

**Institutional Penalties:**
- Administrative fines (up to R10 million per contravention)
- License suspension or revocation
- Reputational damage
- Civil liability for losses

**"Tipping Off" Offense:**
- Informing customer they are subject of STR is a CRIME
- Maximum 15 years imprisonment
- Cannot warn customer about investigation

## What Is Money Laundering?

**Definition:** The process of making illegally-obtained money ("dirty money") appear legitimate ("clean").

### The Three Stages of Money Laundering

**Stage 1: PLACEMENT**

**Definition:** Introducing illegal funds into the financial system.

**Casino Methods:**
- Purchasing chips with cash
- Buying into high-stakes games
- Making large cash deposits
- Using currency exchange services
- Purchasing casino cheques

**Example:**
*Drug trafficker arrives with R500,000 cash in duffel bag. Purchases chips over several days in R24,000 transactions (below reporting threshold). Now the criminal has "placed" dirty cash into the financial system.*

**Red Flags:**
- Large amounts of cash from unknown sources
- Reluctance to provide identification
- Nervousness during large cash transaction
- Arriving with others who also conduct cash transactions
- Structured transactions just below reporting thresholds

**Stage 2: LAYERING**

**Definition:** Separating illegal proceeds from their source through complex layers of financial transactions.

**Casino Methods:**
- Minimal gambling followed by cashing out
- Moving funds between different games/tables
- Using multiple player accounts
- Transferring chips to other players
- Losing deliberately to confederates
- Requesting frequent payment methods changes

**Example:**
*Money launderer purchases R100,000 in chips, plays a few hands of blackjack (losing R5,000), then cashes out R95,000 via casino cheque. Now the money appears to be gambling winnings, not crime proceeds.*

**Red Flags:**
- Little to no actual gambling
- Requesting different payout method than deposit
- Frequent small bets followed by large cash-out
- Elaborate chip transactions with minimal play
- Moving money between multiple accounts rapidly

**Stage 3: INTEGRATION**

**Definition:** Returning laundered funds to criminal as legitimate wealth.

**Casino Methods:**
- Claiming money as gambling winnings
- Using casino cheques to purchase assets
- Wire transferring to overseas accounts
- Obtaining tax documentation for "winnings"
- Investing in legitimate businesses

**Example:**
*After multiple cycles of placement and layering through several casinos, criminal has R5 million in casino cheques and wire transfers appearing to be gambling winnings. Deposits into bank account, buys property, invests in business - now appears to be legitimate wealthy gambler.*

**Red Flags:**
- Large payouts with minimal play history
- Requesting tax documentation for suspicious wins
- Multiple casinos involved in short timeframe
- Winnings not proportional to play observed
- Immediate investment of casino proceeds

## Common Money Laundering Methods in Casinos

### Method 1: Structuring (Smurfing)

**Description:** Breaking large amounts into smaller transactions below reporting thresholds (R24,999.99).

**How It Works:**
- Criminal has R500,000 to launder
- Recruits 20 "smurfs" (accomplices)
- Each makes R24,000 purchase (below R25,000 threshold)
- Repeat over several days
- Aggregate transactions avoid detection

**Indicators:**
- Multiple people arriving together
- Similar transaction amounts just below threshold
- Coordinated timing
- Minimal gambling after purchase
- Same addresses or identification patterns

**Your Response:**
- Aggregate transactions from related parties
- Report suspicious patterns even if individual transactions below threshold
- Document group behaviors
- File STR if structuring suspected

### Method 2: Minimal Gambling

**Description:** Purchase chips, gamble minimally, cash out in different form.

**How It Works:**
- Purchase R100,000 chips with cash
- Play R5,000-R10,000 (small loss acceptable)
- Cash out via cheque or wire transfer
- Money now appears to be gambling proceeds

**Indicators:**
- Large cash purchase followed by minimal play
- Short duration on gaming floor
- Requesting different payout method immediately
- Little interest in actual gambling
- Focus on transaction rather than entertainment

**Your Response:**
- Monitor chip purchase vs. actual play
- Question unusual payout requests
- Document ratio of buy-in to play
- File STR if pattern suspicious

### Method 3: Chip Walking

**Description:** Transferring value between players using chips.

**How It Works:**
- Criminal purchases chips with dirty cash
- "Accidentally drops" chips at table
- Confederate "finds" chips and cashes them out
- Or directly slides chips to confederate
- Confederate now has clean funds

**Indicators:**
- Chip transfers between players
- Large chip denominations changing hands
- Players who don''t know each other sharing chips
- Minimal play after chip exchange
- Coordinated arrival and departure

**Your Response:**
- Monitor chip movements between players
- Question unusual chip transfers
- Document player interactions
- Report suspicious chip walking
- Enforce casino policies on chip transfers

### Method 4: Trade-Based Laundering

**Description:** Using casino environment to conduct business transactions.

**How It Works:**
- Two businesspeople meet at casino
- Appear to be gambling
- Actually conducting illegal transaction
- Casino provides cover and legitimate appearance
- May exchange chips representing business value

**Indicators:**
- Business meetings on gaming floor
- Negotiations visible between players
- Chip exchanges not related to game
- Documents or briefcases present
- Minimal attention to gambling

**Your Response:**
- Observe player interactions
- Note unusual business-like behavior
- Document suspicious meetings
- Report to AML compliance officer

### Method 5: Using Junket Operators

**Description:** Exploiting VIP casino programs and junket operations.

**How It Works:**
- Criminal claims to be legitimate high-roller
- Works with corrupt junket operator
- Transfers dirty money as "gambling funds"
- Junket operator provides clean credit
- Minimal actual gambling occurs
- Funds returned as legitimate winnings

**Indicators:**
- VIP players with unclear background
- Junket relationships lacking transparency
- Credit not proportional to known wealth
- Minimal play despite large credit lines
- Offshore connections and fund movements

**Your Response:**
- Enhanced due diligence on VIP players
- Verify source of wealth and funds
- Monitor actual play vs. credit
- Question unusual junket arrangements
- Report suspicious VIP activity

