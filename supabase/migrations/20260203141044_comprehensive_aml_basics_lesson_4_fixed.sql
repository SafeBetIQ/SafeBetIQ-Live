## Case Study 1: The Cash Courier Network

### Background

Your casino is located near an international border. Over the past month, you''ve observed an unusual pattern involving multiple customers who seem unconnected.

### The Pattern

**Week 1:**
- Monday: Man (approximately 30 years old) purchases R23,000 chips with cash, plays slots briefly (R2,000), cashes out via cheque
- Wednesday: Woman (approximately 25 years old) purchases R22,000 chips with cash, minimal blackjack play (R1,500), cashes out via cheque
- Friday: Different man (approximately 35 years old) purchases R24,000 chips with cash, plays poker briefly (R3,000), cashes out via wire transfer

**Week 2-4:**
- Similar pattern repeats 3-4 times weekly
- Different individuals each time
- Always R20,000-R24,000 amounts
- All pay cash (mix of R50, R100, R200 notes)
- Minimal gambling (5-15% of buy-in)
- Request different payout methods (cheques, wire transfers)
- Various gaming options, always brief sessions

**Additional Observations:**
- All customers are foreign nationals (neighboring country passports)
- Most list occupation as "trader" or "business person"
- Source of funds stated as "business proceeds"
- Several arrive in same vehicle (different days, observed by security)
- Vehicle has foreign plates
- All appear nervous during transactions
- Some struggle to explain source of cash when questioned
- Phone calls observed before answering questions

### Analysis Questions

**Question 1: What Money Laundering Method Is This?**

**Answer:**

This is **Structuring (Smurfing) Combined with Placement/Layering** through a professional cash courier network.

**Indicators:**
- Multiple individuals used (smurfing)
- Transactions just below R25,000 threshold (structuring)
- Coordinated operation (same vehicle, pattern timing)
- Cross-border element (foreign nationals, foreign vehicle)
- Minimal gambling (not genuine customers)
- Different payout methods (layering)
- Cash in mixed denominations (typical of crime proceeds)
- "Business traders" (common courier cover story)

**Assessment:** This is a PROFESSIONAL money laundering operation, likely serving organized crime network or drug trafficking organization operating cross-border.

**Question 2: What Should You Have Done?**

**Answer:**

**Immediate Actions Required:**

**During Transactions:**
- Aggregate all related transactions (treat group as single customer)
- Enhanced questioning about relationships
- Document vehicle information
- Obtain clear identification copies
- Verify passport validity
- Question source of cash more thoroughly
- Record descriptions and behavior

**After Pattern Recognition:**
- File comprehensive STR immediately
- Include all related transactions
- Reference surveillance footage
- Provide vehicle information
- Note cross-border indicators
- Assess total amount laundered (likely R400,000-R800,000 over month)

**Ongoing:**
- Alert all shifts to watch for pattern
- Coordinate with security
- Monitor for continuation
- Consider refusing service if pattern persists
- Brief management fully
- Cooperate with any FIC investigation

**Question 3: Legal Consequences if Not Reported?**

**Answer:**

**Individual Staff Liability:**
- Criminal prosecution for failing to report (FICA violation)
- Up to 15 years imprisonment
- Personal fines up to R10 million
- Civil liability for losses
- Professional disbarment
- Criminal record

**Casino Liability:**
- Administrative fines up to R10 million per contravention
- License suspension or revocation
- Reputational destruction
- Regulatory enhanced monitoring
- Customer confidence loss
- Civil claims from affected parties

**Societal Harm:**
- Enabled serious crime (drugs, trafficking, corruption)
- Undermined law enforcement
- Facilitated harm to victims
- Corrupted financial system

### Learning Points

**This case illustrates:**
- Importance of pattern recognition over time
- Value of cross-shift communication
- Aggregation of related transactions
- Professional launderer sophistication
- Cross-border risk indicators
- Security and operations collaboration necessity

## Case Study 2: The Insider Threat

### Scenario

Maria is a cashier who has worked at your casino for 5 years. She is reliable, popular with customers, and has good performance reviews.

**Concerning Observations:**

**Month 1:**
- Maria processes multiple transactions for customer "David" who visits 2-3 times weekly
- David always requests Maria window
- David conducts R15,000-R20,000 cash transactions
- Maria appears to know David well (friendly conversation)
- David rarely gambles (brief slots sessions)

**Month 2:**
- David transactions increase to R40,000-R50,000
- Still requests Maria exclusively
- Maria no longer requests identification from David
- Maria marks transactions as "verified customer"
- Supervisor notices Maria and David leaving together after Maria shift
- David cash is often wrapped in unusual bundles

**Month 3:**
- Surveillance footage review shows Maria accepts cash from David without counting fully
- Maria provides chips immediately (unusual speed)
- David and Maria exchange glances that appear coordinated
- Maria seems to record lower amounts than cash received
- Maria packages David chips in specific way
- Security observes Maria lifestyle changes (new expensive car, designer clothes)
- David appears in adverse media (suspected drug dealer)
- Colleague reports Maria mentioned "helping a friend with business"

**Today:**
- Another cashier reports David attempted R200,000 transaction when Maria was off shift
- David became agitated when asked for identification and source documentation
- David stated "Maria never asks all these questions"
- David left without completing transaction
- David called Maria on her day off (phone records show multiple calls)

### Analysis Questions

**Question 1: What Is Happening Here?**

**Answer:**

**Suspected Criminal Activity:**

Maria is facilitating money laundering for David through **insider assistance**. This involves:

**Procedural Violations:**
- Not conducting proper CDD
- Falsifying transaction records
- Recording incorrect amounts
- Circumventing controls
- Providing special treatment

**Personal Corruption:**
- Personal relationship with criminal
- Likely receiving payments (evidenced by lifestyle change)
- Coordinating with criminal
- Deliberately facilitating crime

**Money Laundering:**
- David is laundering drug proceeds
- Maria provides inside access
- Controls are bypassed
- Large amounts processed without scrutiny

**Assessment:** This is EMPLOYEE-FACILITATED MONEY LAUNDERING - very serious criminal conduct by both Maria and David.

**Question 2: What Actions Must Be Taken?**

**Answer:**

**Immediate Actions:**

**Suspend Maria:**
- Remove from duties immediately
- Do not allow access to systems
- Preserve evidence
- No warning beforehand

**Investigation:**
- Review ALL of Maria transactions with David
- Examine surveillance footage comprehensively
- Audit Maria transaction records vs. surveillance
- Check for other customers Maria may be helping
- Review Maria financial records (with legal authority)
- Interview colleagues about observations

**Reporting:**
- File comprehensive STR on David
- File STR on Maria (employee suspicious activity)
- Report to HR and legal department
- Consider SAPS referral (criminal conduct)
- Report to National Gambling Board
- Notify FIC of insider threat

**Evidence Preservation:**
- Secure all transaction records
- Preserve surveillance footage
- Document timeline of events
- Collect witness statements
- Maintain chain of custody

**Risk Management:**
- Review other cashiers for similar patterns
- Enhance supervision and controls
- Implement dual controls for large transactions
- Random transaction audits
- Strengthen insider threat monitoring

**Question 3: Preventing Insider Threats?**

**Answer:**

**Prevention Strategies:**

**1. Hiring and Screening:**
- Thorough background checks
- Criminal record verification
- Credit checks (financial stress indicator)
- Reference verification
- Ongoing screening

**2. Supervision and Controls:**
- Dual controls for large transactions
- Random transaction audits
- Surveillance monitoring
- Supervisor approval requirements
- Rotation of duties

**3. Culture and Ethics:**
- Strong ethical culture
- Whistleblower protection
- Anonymous reporting channels
- No tolerance for violations
- Leadership modeling integrity

**4. Detection Systems:**
- Transaction monitoring for staff patterns
- Lifestyle monitoring (significant changes)
- Exception reporting
- Peer review processes
- Regular compliance audits

**5. Training and Awareness:**
- Ethics training
- Consequences of violations
- Recognizing insider threats
- Reporting obligations
- Support for reporting concerns

**6. Response Capability:**
- Clear escalation procedures
- Investigation protocols
- Legal support available
- Swift disciplinary action
- Coordination with authorities

### Learning Points

**This case illustrates:**
- Insider threats are serious vulnerability
- Personal relationships can be exploited
- Lifestyle changes may indicate corruption
- Supervision and monitoring are essential
- Swift action required when discovered
- Prevention through controls and culture

## Professional Competence Assessment

### Section A: Knowledge Questions (Select Best Answer)

**Question 1:** The three stages of money laundering are:
- A) Detection, Investigation, Prosecution
- B) Placement, Layering, Integration
- C) Collection, Distribution, Investment
- D) Planning, Execution, Concealment

**Answer: B** - Placement, Layering, Integration

**Question 2:** Under FICA, you must verify identity for cash transactions above:
- A) R5,000
- B) R10,000
- C) R24,999.99
- D) R50,000

**Answer: A** - R5,000 (but note: CTR threshold is R24,999.99)

**Question 3:** "Tipping off" means:
- A) Reporting suspicious transactions to FIC
- B) Warning a customer they are subject of investigation or report
- C) Giving good service to high-value customers
- D) Providing information to law enforcement

**Answer: B** - Warning customer they are subject of investigation or report (CRIMINAL OFFENSE)

**Question 4:** Enhanced Due Diligence is required for:
- A) All customers without exception
- B) Politically exposed persons and high-risk customers
- C) Only foreign nationals
- D) Customers who request it

**Answer: B** - Politically exposed persons and high-risk customers

**Question 5:** "Structuring" or "smurfing" means:
- A) Building relationships with criminals
- B) Breaking large transactions into smaller amounts below reporting threshold
- C) Organizing casino operations efficiently
- D) Layering multiple types of transactions

**Answer: B** - Breaking large transactions into smaller amounts below reporting threshold

**Question 6:** Records must be kept for minimum:
- A) 1 year
- B) 3 years
- C) 5 years
- D) 10 years

**Answer: C** - 5 years after relationship ends

**Question 7:** You should file an STR when:
- A) You are certain money is from crime
- B) You have reasonable grounds to suspect laundering
- C) Customer requests you to file one
- D) Only when instructed by manager

**Answer: B** - Reasonable grounds to suspect laundering (suspicion is enough; proof not required)

**Question 8:** PEP stands for:
- A) Preferred Elite Player
- B) Protected Enterprise Person
- C) Politically Exposed Person
- D) Primary Enforcement Priority

**Answer: C** - Politically Exposed Person

**Question 9:** Maximum penalty for individual FICA violations can include:
- A) Written warning only
- B) Small fine (R10,000)
- C) Up to 15 years imprisonment and R10 million fine
- D) Casino liability only (no personal liability)

**Answer: C** - Up to 15 years imprisonment and R10 million fine (SERIOUS PERSONAL LIABILITY)

**Question 10:** When conducting CDD, you should:
- A) Only verify identity, nothing else needed
- B) Identify, verify identity, assess risk, understand transaction purpose
- C) Simply check ID and proceed
- D) Only question suspicious-looking people

**Answer: B** - Identify, verify identity, assess risk, understand transaction purpose (COMPREHENSIVE CDD)

### Section B: Scenario Analysis

**Scenario:**

Customer presents with R180,000 cash in mixed denominations. States he sold his car. He is nervous, provides ID when asked, says he wants to "try gambling for first time." Purchases chips, plays R8,000 blackjack for 30 minutes, requests wire transfer payout to offshore account.

**Questions:**
1. What red flags are present?
2. What should you do?
3. Would you file STR?

**Model Answer:**

**Red Flags Present:**
- Very large cash amount (R180,000)
- Mixed denominations (typical of crime proceeds)
- Source story questionable (car sale would typically be bank transfer)
- Nervousness
- "First time" yet comfortable with large amount
- Minimal gambling (R8,000 of R180,000 = 4.4%)
- Different payout method (cash in, wire transfer out)
- Offshore account destination
- Classic placement and layering pattern

**What to Do:**
- Complete full CDD (verify identity, source of funds)
- Question car sale (request documentation)
- Ask about offshore account relationship
- Document nervous behavior
- Calculate play-to-buy-in ratio
- Verify account holder for wire transfer
- Consult supervisor
- Process transaction normally but document concerns

**File STR:** YES - Multiple red flags indicating money laundering. File comprehensive STR detailing:
- Transaction specifics
- Source story concerns
- Minimal gambling pattern
- Payout method change
- Offshore destination
- Behavioral concerns
- Risk assessment: HIGH

